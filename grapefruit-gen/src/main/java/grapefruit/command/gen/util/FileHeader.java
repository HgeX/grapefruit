package grapefruit.command.gen.util;

public final class FileHeader {
    public static final String CORNER = "+";
    public static final String VERTICAL_SEPARATOR = "|";
    public static final String HORIZONTAL_SEPARATOR = "-";
    public static final String LINE_1 = "This file was generated by Grapefruit. Please DO NOT attempt to modify it.";
    public static final String LINE_2 = "Generated:";
    public static final String LINE_3 = "- By: %s";
    public static final String LINE_4 = "- At: %s";

    private FileHeader() {}

    public static void writeHorizontal(StringBuilder builder, int length) {
        builder.append(CORNER)
                .append(HORIZONTAL_SEPARATOR.repeat(length))
                .append(CORNER)
                .append("\n");
    }

    public static void writeBlank(StringBuilder builder, int length) {
        builder.append(VERTICAL_SEPARATOR)
                .append(" ".repeat(length))
                .append(VERTICAL_SEPARATOR)
                .append("\n");
    }

    public static void writeLine(StringBuilder builder, int length, String line) {
        builder.append(VERTICAL_SEPARATOR)
                .append(" ")
                .append(line)
                .append(" ".repeat(length - 2 - line.length()))
                .append(" ")
                .append(VERTICAL_SEPARATOR)
                .append("\n");
    }
}
